    private void jButtonComputeSalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComputeSalaryActionPerformed
        // Validate combo box selections
        int startMonth = jComboBoxStartMonth.getSelectedIndex();
        int endMonth = jComboBoxEndMonth.getSelectedIndex();
        int startDay = jComboBoxStartDay.getSelectedIndex();
        int endDay = jComboBoxEndDay.getSelectedIndex();
        int startYear = jComboBoxStartYear.getSelectedIndex();
        int endYear = jComboBoxEndYear.getSelectedIndex();
        int workDays = jComboBoxWorkDays.getSelectedIndex();

        // Check if any combo box is not selected
        if (startMonth == 0 || endMonth == 0 || startDay == 0 || endDay == 0 || startYear == 0 || endYear == 0 || workDays == 0) {
            JOptionPane.showMessageDialog(this, "Please select valid options for all fields before proceeding.", "Invalid Selection", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Check if month selections are for 1-month cutoff period
        if (!(startMonth == endMonth || startMonth == endMonth - 1)) {
            JOptionPane.showMessageDialog(this, "Please select valid months as this program only computes for monthly payslips.", "Invalid Selection", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Calculate the total number of days in the cutoff period
        int totalDays;
        if (startMonth == endMonth) {
            totalDays = endDay - startDay + 1;
        } else {
            totalDays = (31 - startDay + 1) + endDay;
        }
        
        // Validate that total days in the cutoff period do not exceed 31 days
        if (totalDays > 31) {
            JOptionPane.showMessageDialog(this, "Invalid date range. The start and end dates should not span more than one month.", "Invalid Selection", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate that workdays do not exceed total days in the cutoff period
        if (workDays > totalDays) {
            JOptionPane.showMessageDialog(this, "Invalid number of workdays. Please ensure that the number of workdays does not \nexceed the total days in the cutoff period based on the inputted start and end dates.", "Invalid Selection", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String[] workPeriod = getWorkPeriod();
        int daysWorked = getDaysWorked();
        PayslipPage payslipPage = new PayslipPage(employeeNumber, workPeriod[0], workPeriod[1], daysWorked);
        payslipPage.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButtonComputeSalaryActionPerformed

