    private void addDateRangeListener() {
        ItemListener itemListener = new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    updateWorkPeriodTextField();
                    getDaysWorked();
                }
            }
        };

        jComboBoxStartMonth.addItemListener(itemListener);
        jComboBoxEndMonth.addItemListener(itemListener);
        jComboBoxStartDay.addItemListener(itemListener);
        jComboBoxEndDay.addItemListener(itemListener);
        jComboBoxStartYear.addItemListener(itemListener);
        jComboBoxEndYear.addItemListener(itemListener);  
        jComboBoxWorkDays.addItemListener(itemListener);
    }
    
    
    public String[] getWorkPeriod() {
        return updateWorkPeriodTextField();
    }
    
    
    private String[] updateWorkPeriodTextField() {
        int startMonth = jComboBoxStartMonth.getSelectedIndex();
        int endMonth = jComboBoxEndMonth.getSelectedIndex();
        int startDay = jComboBoxStartDay.getSelectedIndex();
        int endDay = jComboBoxEndDay.getSelectedIndex();
        String startYear = (String) jComboBoxStartYear.getSelectedItem();
        String endYear = (String) jComboBoxEndYear.getSelectedItem();
        String startDate = String.format("%02d/%02d/%s", startMonth, startDay, startYear);
        String endDate = String.format("%02d/%02d/%s", endMonth, endDay, endYear);
        return new String[]{startDate, endDate};
    }
    
    
       public int getDaysWorked() {
        int daysWorked = jComboBoxWorkDays.getSelectedIndex();
        return daysWorked;
    }
    
       
    public int getHoursWorked(int daysWorked) {
        int hoursWorked = daysWorked * 8;
        return hoursWorked;
    }
    
